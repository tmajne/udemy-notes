<?php

/*
W PHP, przestrzenie nazw (ang. namespaces) są mechanizmem służącym do organizowania kodu w logiczne grupy oraz unikania konfliktów nazw.
Przestrzenie nazw umożliwiają podział kodu na logiczne sekcje, co ułatwia zarządzanie i utrzymanie projektu.

Przestrzenie nazw pozwalają na definiowanie unikalnych identyfikatorów dla klas, funkcji, stałych i innych elementów kodu.
Działają one podobnie jak foldery w systemie plików, tworząc hierarchię, w której poszczególne elementy kodu są grupowane.

Aby zdefiniować przestrzeń nazw w PHP, używa się słowa kluczowego "namespace". Przykładem jest:
 */

namespace MojaPrzestrzenNazw;

class MojaKlasa {
    // kod klasy
}

function mojaFunkcja() {
    // kod funkcji
}



// Przestrzenie nazw w PHP są szczególnie przydatne w większych projektach, gdzie istnieje wiele klas,
// funkcji i innych elementów kodu. Umożliwiają one unikanie konfliktów nazw i ułatwiają organizację kodu.